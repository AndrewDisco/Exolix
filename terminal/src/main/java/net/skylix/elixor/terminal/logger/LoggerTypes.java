package net.skylix.elixor.terminal.logger;

enum LoggerTypes {
    /**
     * Informative message.
     */
    INFO,

    /**
     * Warning message.
     */
    WARNING,

    /**
     * Error message.
     */
    ERROR,

    /**
     * Debug message.
     */
    DEBUG,

    /**
     * Verbose message.
     */
    VERBOSE,

    /**
     * Deprecated message.
     */
    DEPRECATED,

    /**
     * Success message.
     */
    SUCCESS
}
